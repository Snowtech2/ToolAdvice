    public NewJFrame() {
        initComponents();
        getContentPane().setBackground(java.awt.Color.getHSBColor(0.4f,0.3f,0.6f));
       
        try{  
            BufferedReader CSVFile = new BufferedReader(new FileReader("resources.csv"));
      
            LinkedList<String[]> rows = new LinkedList<String[]>();
            String dataRow;
            int i = 0; 
            while (((dataRow = CSVFile.readLine()) != null)&&(i<200)){
                i++;
                rows.addLast(dataRow.split(","));
            }  
            String[][] csvMatrix = rows.toArray(new String[rows.size()][]);
            for (n=0;n<i;n++){
                if (Integer.parseInt(csvMatrix[n][1])>0){
                    jComboBox1.addItem(csvMatrix[n][0]+" ("+csvMatrix[n][1]+")");
                } else
                    jComboBox1.addItem(csvMatrix[n][0]);
                dif[n]=Double.parseDouble(csvMatrix[n][1]);
            }
        
        }catch (FileNotFoundException e){
            for (n=0;n<63;n++){
                if (diffs[n]>0){
                    jComboBox1.addItem(actnames[n]+" ("+(int)diffs[n]+")");
                } else
                    jComboBox1.addItem(actnames[n]);
                dif[n]=diffs[n];
            }
        }catch (IOException e) {
            jLabel3.setText("Read error");
        }          
    }

