    private void CalculateQL(){
    double d,s,q;
    int ql;
    
        d=dif[jComboBox1.getSelectedIndex()];
        if ((d>0)&&(!jTextField1.getText().isEmpty())){
            s=Float.valueOf(jTextField1.getText());
            q=(d+20)*2-s;
            ql=(int)q;
            if (ql<1)
                ql=1;
            if (ql>100)
                ql=100;
            jTextField2.setText(String.valueOf(ql));        
        } else jTextField2.setText("");
        
    }
